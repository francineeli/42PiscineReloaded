int	ft_strcmp(char *s1, char *s2)
{
	int	tam;

	tam = 0;
	while (s1[tam] == s2[tam] && s1[tam] != '\0' && s2[tam] != '\0')
		tam++;
	return (s1[tam] - s2[tam]);
}
